// string data type
let nama: string = "daffa"
nama = "satria"

// number data type
let umur: number = 12
umur = 21

// boolean data type
let isValid: boolean = true
isValid = false

// any data type (can use all type data in 1 variabel)
let random: any = "jokowi"
random = 12
random = true
random = []

// union data type
let phoneNumber: number | string
phoneNumber = 6281296222133
phoneNumber = "081295434323"